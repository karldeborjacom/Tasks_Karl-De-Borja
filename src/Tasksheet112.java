public class Tasksheet112 {
    public static void main(String[] args) {
        // Declare the required primitives
        char h = 'H';      // for "H"
        int three = 3;     // for "3"
        int one = 1;       // for "1"
        int zero = 0;      // for "0"
        char w = 'w';      // for "w"
        char r = 'r';      // for "r"
        char d = 'd';      // for "d"
        float twoPointZero = 2.0f;  // for "2.0"
        boolean isTrue = true;      // for "true"

        // Concatenate them into a single string
        String output = "" + h + three + one + one + zero + " " + w + 0 + r + one + d + " " + twoPointZero + " " + isTrue;

        // Print the result
        System.out.println(output);
    }
}
